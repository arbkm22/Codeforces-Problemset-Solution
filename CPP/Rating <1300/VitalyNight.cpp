#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    int n, m;
    cin >> n >> m;
    int ans = 0;
    vector<int> a(n*(m*2));
    for (auto &x:a)
        cin >> x;
    for (int i=0; i<a.size(); i+=2) {
        for (int j=i; j<i+2; j++) {
            if (a[j] == 1) {
                ans++;
                break;
            }
        }
    }
    cout << ans << endl;

    return 0;
}
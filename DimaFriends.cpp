#include <bits/stdc++.h>
#include <vector>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n;
    cin >> n;
    int sum = 0, ans = 0;
    for (int i=1; i<=n; i++) {
        int x;
        cin >> x;
        sum += x;
    }
    for (int i=5; i>=1; i--) {
        if ((sum + i) % (n + 1) != 1)
            ans++;
    }
    cout << ans << endl;

    return 0;
}

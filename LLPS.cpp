#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    string s;
    cin >> s;
    char largest = 'a';
    for (int i=0; i<s.size(); i++) {
        if (s[i] > largest)
            largest = s[i];
    }
    int cnt = count(s.begin(), s.end(), largest);
    for (int i=0; i<cnt; i++) {
        cout << largest;
    }
    cout << endl;

    return 0;
}
#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n;
    cin >> n;
    bool state = false;
    vector<int> a(n);
    for (auto &x:a) {
        cin >> x;
        if (x==1)
            state = true;
    }
    sort(a.begin(), a.end());
    for (int i=0; i<a.size(); i++) {
        for (int j=i+1; j<a.size(); j++) {
            if (a[j]%a[i]==0)
                a.erase(a.begin()+j);
        }
    }
    for (int i=0; i<a.size(); i++) {
        for (int j=i+1; j<a.size(); j++) {
            if (a[j]%a[i]==0)
                a.erase(a.begin()+j);
        }
    }
    if (state)
        cout << 1 << endl;
    else
        cout << a.size() << endl;

    return 0;
}
#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n, m;
    cin >> n >> m;
    vector<int> a(m+1);
    for (int i=1; i<=n; i++) {
        int l, r;
        cin >> l >> r;
        for (int i=l; i<=r; i++)
            a[i]++;
    }
    vector<int> ans;
    for (int i=1; i<=m; i++) {
        if (a[i]==0)
            ans.push_back(i);
    }
    cout << ans.size() << endl;
    for (auto &x:ans)
        cout << x << " ";
    cout << endl;

    return 0;
}
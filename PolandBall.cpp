#include <bits/stdc++.h>
using namespace std;

#define ll long long

bool isPrime(int x) {
    if (x==1)
        return false;
    if (x>=2) {
        for (int i=2; i<x; i++) {
            if (x%i==0)
                return false;
        }
    }
    return true;
}

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n;
    cin >> n;
    for (int i=1; i<1000; i++) {
        if (!isPrime((n*i)+1)) {
            cout << i << endl;
            break;
        }
    }

    return 0;
}
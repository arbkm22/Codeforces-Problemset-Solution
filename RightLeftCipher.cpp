#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    string s, ans;   
    cin >> s;
    int n = s.size();
    int k, i, j;
    if (n%2==1)
        k = n/2;
    else
        k = (n/2)-1;
    ans.push_back(s[k]);
    i = k+1;
    j = k-1;
    while (ans.size()<n) {
        if (i<n) {
            ans.push_back(s[i]);
            i++;
        }
        if (j>=0) {
            ans.push_back(s[j]);
            j--;
        }
    }
    cout << ans << endl;
    return 0;
}
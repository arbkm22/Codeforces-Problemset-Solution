#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    string s, ans;   
    cin >> s;
    if (s.size()==1) {
        cout << s << endl;
        return 0;
    }
    int f = (s.size()/2)-1;
    int right = f+1;
    int left = f-1;
    ans += s[f];
    for (int i=0; i<s.size()-1; i++) {
        if (i%2==0) {
            ans.push_back(s[right]);
            right++;
        }
        else {
            ans.push_back(s[left]);
            left--;
        }
    }
    cout << ans << endl;
    return 0;
}
#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n, m, r;
    cin >> n >> m >> r;
    vector<int> a(n), b(m);
    for (auto &x:a)
        cin >> x;
    for (auto &y:b)
        cin >> y;
    sort(a.begin(), a.end());
    sort(b.begin(), b.end());
    if (a[0] < b[m-1]) {
        int tmp = r/a[0];
        r -= (tmp*a[0]);
        r += (tmp*b[m-1]);
    }   
    cout << r << endl;

    return 0;
}
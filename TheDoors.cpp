#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int n;
    cin >> n;
    vector<int> a(n);
    for (auto &x:a)
        cin >> x;
    int doors = 0;
    int l = count(a.begin(), a.end(), 0);
    int r = count(a.begin(), a.end(), 1);
    int l1=0, r1=0;
    for (int i=0; i<a.size(); i++) {
        if (a[i]==0) {
            doors++;
            l1++;
        }
        else if (a[i]==1) {
            doors++;
            r1++;
        }
        if (l1==l || r1==r) 
            break;
    }
    cout << doors << endl;

    return 0;
}
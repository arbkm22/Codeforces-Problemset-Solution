#include <bits/stdc++.h>
using namespace std;

#define ll long long

int main(void) {
    ios::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    int t;
    cin >> t;
    while (t--) {
        int n, k;
        cin >> n >> k;
        vector<int> a(n);
        for (auto &x:a)
            cin >> x;
        for (int i=0; i<n-1; i++) {
            if (a[i]<k) {
                k -= a[i];
                a[n-1] += a[i];
                a[i] = 0;
            }
            else {
                a[i] -= k;
                a[n-1] += k;
                k = 0;
            }
        }
        for (auto &x:a)
            cout << x << " ";
        cout << endl;
    }

    return 0;
}